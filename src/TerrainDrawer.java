    public TerrainDrawer(BufferedImage terrainImage) {
        this.terrainImage = terrainImage;
    }

    public void drawBlock(int x, int y, Block block) {
        int[][] blockColors;
 
        if (block == Block.GRASS) {
            blockColors = GRASS;
        } else if (block == Block.DIRT) {
            blockColors = DIRT;
        } else if (block == Block.CAVE) {
            blockColors = CAVE;
        } else if (block == Block.STONE) {
            blockColors = STONE;
        } else {
            return;
        }

        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                terrainImage.setRGB(x + j, y + i, blockColors[i][j]);
            }
        }
    }

    public void drawSky(int i, int j) {
        for (int x = i; x < i + 8; x++) {
            for (int y = j; y < j + 8; y++) {
                terrainImage.setRGB(x, y, 0x0045FF);
            }
        }
    }
    
